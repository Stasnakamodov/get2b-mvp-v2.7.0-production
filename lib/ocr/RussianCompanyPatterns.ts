  let bonus = 0;
  const matchText = match[0].toLowerCase();
  
  // –ë–æ–Ω—É—Å –∑–∞ –∫–æ–Ω—Ç–µ–∫—Å—Ç–Ω—ã–µ —Å–ª–æ–≤–∞
